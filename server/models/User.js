const mongoose = require('mongoose')

const UserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true, 
        minlength: 2,
    },
    password: {
        type: String,
        minlength: 4,
        required: true,
    },
    refreshToken: {
        type: String,
        default: "",
    },
});
module.exports = mongoose.model('User', UserSchema)

